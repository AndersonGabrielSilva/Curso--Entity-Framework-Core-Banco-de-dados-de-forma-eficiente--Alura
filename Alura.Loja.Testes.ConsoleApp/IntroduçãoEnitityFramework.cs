         * mais facil e produtiva, pois ele não precisa se preoculpar tanto com os comandos SQL e se dedica
         * mais ao desenvolvimento do codigo em si.
         * Tambem ao ultilizar o Entity fica mais facil para o softwae evoluir pois ele é responsael por escrever,
         * montar o parametro e converter os dados para o Banco.
         * O Entity é um "Mapeamento Objeto Relacional (ORM)", ele não é o unico existe varios outros. Ele é apenas um deles.
         * Mapeamento objeto-relacional é uma técnica de desenvolvimento utilizada para reduzir a impedância da 
         * programação orientada aos objetos utilizando bancos de dados relacionais
